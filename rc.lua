-- {{{ Helper functions
local function sorted_tag_index(screen, name)
    local i = 1
    while screen.tags[i] do
        if name < screen.tags[i].name then break end
        i = i + 1
    end
    return i
end

local function provision_screen(screen)
    local scratch_tag_name = "*scratch*"
    for _, t in pairs(screen.tags) do
        if t.name == scratch_tag_name then return end
    end
    new_tag = awful.tag.add(scratch_tag_name, {
        screen = screen,
        index = sorted_tag_index(screen, scratch_tag_name),
        layout = awful.layout.layouts[1]
    })
    if #screen.selected_tags == 0 then
        new_tag.selected = true
    end
end

local function switch_tag_prompt(on_switch)
    local function create_tag(name)
        new_tag = awful.tag.add(name, {
            screen = awful.screen.focused(),
            index  = sorted_tag_index(awful.screen.focused(), name),
            layout = awful.layout.layouts[1]
        })
        if on_switch then on_switch(new_tag) end
        new_tag:view_only()
    end
    local function switch_tag(tag)
        if on_switch then on_switch(tag) end
        tag:view_only()
    end
    rofi.select_tag_prompt(switch_tag, create_tag)
end

local function switch_tag_moving_client_prompt()
    switch_tag_prompt(function(tag)
        if client.focus then
            client.focus:tags({tag})
        end
    end)
end

function rename_tag_prompt()
    s = awful.screen.focused()
    local selected_tag = s.selected_tag
    rofi.ask_for_tag_name("rename tag", function(new_name)
        selected_tag.name = new_name
        selected_tag.index = sorted_tag_index(s, new_name)
        provision_screen(s)
    end)
end
